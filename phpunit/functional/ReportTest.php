namespace tests\units;

use DBTestCase;
use Report;

class ReportTest extends DBTestCase
{
    public function testShowNetworkReport(): void
    {
        // Act: run the method to trigger template rendering
        $this->login();
        ob_start();
        Report::showNetworkReport(null, 0);
        $output = ob_end_clean();

        // Assert: make sure no errors are some html was generated
        $this->assertNotEmpty($output);
    }
}
